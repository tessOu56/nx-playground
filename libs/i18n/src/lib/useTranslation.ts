export type TranslationKeys = CommonTranslationKeys | UITranslationKeys;

export const useTranslation = () => {
  const { t, i18n, ready } = useI18nTranslation(['common', 'ui']);

  const translate = (key: TranslationKeys, options?: any) => {
    return t(key, options);
  };

  return {
    t: translate,
    i18n,
    ready,
    currentLanguage: i18n.language,
    changeLanguage: i18n.changeLanguage,
  };
};
